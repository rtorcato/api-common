// Generate the "Packages" table in README.md from each packages/*/package.json,
// so the table can't drift when a package is added or its description changes.
// `package.json` (name + description) is the single source of truth.
//
//   node scripts/gen-readme-packages.mjs          # rewrite the table in place
//   node scripts/gen-readme-packages.mjs --check   # exit 1 if README is stale (CI)

import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(here, '..')
const packagesDir = resolve(repoRoot, 'packages')
const readmePath = resolve(repoRoot, 'README.md')

const START = '<!-- PACKAGES:START - generated by scripts/gen-readme-packages.mjs -->'
const END = '<!-- PACKAGES:END -->'

const pkgs = readdirSync(packagesDir, { withFileTypes: true })
	.filter((d) => d.isDirectory())
	.map((d) => JSON.parse(readFileSync(resolve(packagesDir, d.name, 'package.json'), 'utf8')))
	.filter((p) => p.name && !p.private)
	.sort((a, b) => a.name.localeCompare(b.name))

const rows = pkgs.map((p) => {
	const dir = `./packages/${p.name.replace('@rtorcato/', '')}`
	const badge = `[![npm](https://img.shields.io/npm/v/${p.name}.svg)](https://www.npmjs.com/package/${p.name})`
	return `| [\`${p.name}\`](${dir}) | ${badge} | ${p.description ?? ''} |`
})

const table = [START, '| Package | npm | Description |', '| --- | --- | --- |', ...rows, END].join(
	'\n'
)

const readme = readFileSync(readmePath, 'utf8')
const region = new RegExp(`${escapeRe(START)}[\\s\\S]*?${escapeRe(END)}`)
if (!region.test(readme)) {
	console.error(`README.md is missing the ${START} ... ${END} markers.`)
	process.exit(1)
}
const next = readme.replace(region, table)

if (process.argv.includes('--check')) {
	if (next !== readme) {
		console.error('README.md package table is out of date. Run `pnpm gen:readme`.')
		process.exit(1)
	}
	console.log('README.md package table is up to date.')
} else {
	writeFileSync(readmePath, next)
	console.log(`Wrote ${pkgs.length} packages to README.md.`)
}

function escapeRe(s) {
	return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}
